def tr(lang: str, key: str, **kwargs: object) -> str:
    table = TEXT.get(lang) or TEXT[DEFAULT_LANG]
    text = table.get(key) or TEXT[DEFAULT_LANG].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    return text
